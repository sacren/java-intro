/**
 * Shape interface for Rectangle and Triangle subclasses.
 *
 * <p>Public interface:
 * +getArea():double
 */
public interface Shape { /* keyword "interface", not "class" */
    /* "public" and "abstract" are assumed.
     * "protected", "private" and "package" are not allowed.
     * Method signature will be implemented in subclasses.
     */
    double getArea();
}
